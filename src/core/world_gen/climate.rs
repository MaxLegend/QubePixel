#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_zone_mapping() {
        // Arctic (temp ≈ 1.0)
        let (zone, idx) = ClimateLayer::temperature_to_zone(1.0);
        assert_eq!(zone, ClimateType::Arctic);
        assert_eq!(idx, 0);

        // Equatorial (temp ≈ 0.0)
        let (zone, idx) = ClimateLayer::temperature_to_zone(0.0);
        assert_eq!(zone, ClimateType::Equatorial);
        assert_eq!(idx, 6);

        // Antarctic (temp ≈ -1.0)
        let (zone, idx) = ClimateLayer::temperature_to_zone(-1.0);
        assert_eq!(zone, ClimateType::Antarctic);
        assert_eq!(idx, 12);
    }

    #[test]
    fn test_zone_boundaries() {
        // Just below Arctic boundary
        let (zone, _) = ClimateLayer::temperature_to_zone(0.84);
        assert_eq!(zone, ClimateType::Subarctic);

        // Just above Equatorial (northern side)
        let (zone, _) = ClimateLayer::temperature_to_zone(0.08);
        assert_eq!(zone, ClimateType::NorthSubequatorial);

        // Just below Equatorial (southern side)
        let (zone, _) = ClimateLayer::temperature_to_zone(-0.08);
        assert_eq!(zone, ClimateType::SouthSubequatorial);
    }

    #[test]
    fn test_clamping() {
        // Extremes beyond [-1, 1] should clamp
        let (_, idx_high) = ClimateLayer::temperature_to_zone(2.0);
        assert_eq!(idx_high, 0);

        let (_, idx_low) = ClimateLayer::temperature_to_zone(-2.0);
        assert_eq!(idx_low, 12);
    }

    #[test]
    fn test_humidity_range() {
        let layer = ClimateLayer::new(42);
        for x in 0..50 {
            for z in 0..50 {
                let info = layer.sample(x * 200, z * 200);
                assert!(
                    (0.0..=1.0).contains(&info.humidity),
                    "humidity out of range: {}",
                    info.humidity
                );
            }
        }
    }

    #[test]
    fn test_sample_area_size() {
        let layer = ClimateLayer::new(42);
        let results = layer.sample_area(0, 0, 16);
        assert_eq!(results.len(), 256);
    }

    #[test]
    fn test_deterministic() {
        let layer = ClimateLayer::new(777);
        let a = layer.sample(500, 500);
        let b = layer.sample(500, 500);
        assert_eq!(a.temperature, b.temperature);
        assert_eq!(a.humidity, b.humidity);
        assert_eq!(a.climate_type, b.climate_type);
    }

    #[test]
    fn test_all_zones_present() {
        let layer = ClimateLayer::new(42);
        let mut found = [false; 13];
        // Scan a large area along Z
        for z in 0..32000 {
            let info = layer.sample(0, z);
            found[info.zone_index as usize] = true;
        }
        for (i, &present) in found.iter().enumerate() {
            assert!(present, "Zone {} not found in scan", i);
        }
    }

    #[test]
    fn test_climate_type_properties() {
        assert!(ClimateType::Arctic.is_northern());
        assert!(!ClimateType::Arctic.is_southern());
        assert!(ClimateType::Equatorial.is_equatorial());
        assert!(ClimateType::SouthTropical.is_southern());
        assert!(!ClimateType::SouthTropical.is_northern());
    }
}
